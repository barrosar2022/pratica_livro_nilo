num1 = int(input())
num2 = int(input())
op = input()

if op == '+':
    print(f'resutado da soma: {num1+num2}')
elif op == '-':
    print(f'resutado da subtração: {num1-num2}')
elif op == '*':
    print(f'resutado da multiplicação: {num1*num2}')
elif op == '/':
    print(f'resutado da divisão: {num1/num2}')

print('fim do programa.')